import type { ConjugationSet, Verb } from '../../types';

// A second batch of common English verbs, generated from a small regular-conjugation
// engine (-s/-es for 3rd person singular, -ed/-d for past, consonant-doubling for
// short CVC verbs where the `double` flag is set, y->ied for consonant+y endings).
type Entry = { infinitive: string; translation: string; double?: boolean };

const SIBILANT_END = /(s|x|z|ch|sh)$/;
const CONSONANT_Y = /[^aeiou]y$/;

function thirdPerson(base: string): string {
  if (SIBILANT_END.test(base)) return `${base}es`;
  if (CONSONANT_Y.test(base)) return `${base.slice(0, -1)}ies`;
  return `${base}s`;
}

function pastForm(base: string, double?: boolean): string {
  if (double) return `${base}${base.at(-1)}ed`;
  if (CONSONANT_Y.test(base)) return `${base.slice(0, -1)}ied`;
  if (base.endsWith('e')) return `${base}d`;
  return `${base}ed`;
}

function conjugate(entry: Entry): Omit<Verb, 'id' | 'languageId'> {
  const { infinitive: base, translation, double } = entry;
  const s3 = thirdPerson(base);
  const past = pastForm(base, double);

  const present: ConjugationSet = [base, base, s3, base, base, base];
  const pastSet: ConjugationSet = [past, past, past, past, past, past];
  const future: ConjugationSet = [
    `will ${base}`, `will ${base}`, `will ${base}`,
    `will ${base}`, `will ${base}`, `will ${base}`,
  ];
  const haveForms = ['have', 'have', 'has', 'have', 'have', 'have'];
  const presentPerfect = haveForms.map(h => `${h} ${past}`) as unknown as ConjugationSet;

  return { infinitive: base, translation, present, past: pastSet, future, presentPerfect };
}

